# 主函数
main() {
    local topic=""
    local output_file=""
    local word_count=6000
    local frameworks="SWOT,PESTEL"
    local smart_flag=false
    local verbose=false

    # 解析参数
    while [[ $# -gt 0 ]]; do
        case $1 in
            -t|--topic)
                topic="$2"
                shift 2
                ;;
            -o|--output)
                output_file="$2"
                shift 2
                ;;
            -w|--word-count)
                word_count="$2"
                shift 2
                ;;
            -f|--framework)
                frameworks="$2"
                shift 2
                ;;
            -s|--smart)
                smart_flag=true
                shift
                ;;
            -v|--verbose)
                verbose=true
                shift
                ;;
            -h|--help)
                show_usage
                exit 0
                ;;
            *)
                echo "未知参数：$1"
                show_usage
                exit 1
                ;;
        esac
    done

    # 验证必需参数
    if [[ -z "$topic" || -z "$output_file" ]]; then
        echo "错误：缺少必需参数"
        show_usage
        exit 1
    fi

    if [[ "$verbose" == true ]]; then
        echo "生成报告..."
        echo "  主题：$topic"
        echo "  输出：$output_file"
        echo "  字数：$word_count"
        echo "  框架：$frameworks"
        echo "  SMART: $smart_flag"
    fi

    # 生成大纲
    generate_outline "$topic" "$output_file"

    if [[ "$verbose" == true ]]; then
        echo "✓ 报告大纲已生成：$output_file"
        echo ""
        echo "下一步："
        echo "1. 使用 AI 工具填充内容"
        echo "2. 收集数据并标注来源"
        echo "3. 应用分析框架（$frameworks）"
        echo "4. 生成建议并确保 SMART 原则"
        echo "5. 进行质量自评"
    fi
}

# 执行主函数
main "$@"
